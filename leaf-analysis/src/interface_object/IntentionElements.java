package interface_object;

public class IntentionElements {

	String id;
	boolean[] status;
	
	public String getId() {
		return id;
	}
	
	public void setId(String id) {
		this.id = id;
	}
	
	public boolean[] getStatus() {
		return status;
	}
	
	public void setStatus(boolean[] status) {
		this.status = status;
	}
	
	
}
